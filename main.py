print("\n1) Критерий χ² Пирсона:")

k = int(1 + 3.322 * np.log10(n))
observed_freq, bin_edges = np.histogram(x, bins=k)

expected_freq_norm = []
for i in range(len(bin_edges) - 1):
    z_low = (bin_edges[i] - mean_val) / std_val
    z_high = (bin_edges[i + 1] - mean_val) / std_val
    prob = norm.cdf(z_high) - norm.cdf(z_low)
    expected_freq_norm.append(n * prob)
expected_freq_norm = np.array(expected_freq_norm)

# Объединяем интервалы с частотой < 5
while np.any(expected_freq_norm < 5) and len(expected_freq_norm) > 3:
    idx = np.argmin(expected_freq_norm)
    if idx == 0:
        expected_freq_norm[1] += expected_freq_norm[0]
        observed_freq[1] += observed_freq[0]
    elif idx == len(expected_freq_norm) - 1:
        expected_freq_norm[-2] += expected_freq_norm[-1]
        observed_freq[-2] += observed_freq[-1]
    else:
        if expected_freq_norm[idx - 1] < expected_freq_norm[idx + 1]:
            expected_freq_norm[idx - 1] += expected_freq_norm[idx]
            observed_freq[idx - 1] += observed_freq[idx]
        else:
            expected_freq_norm[idx + 1] += expected_freq_norm[idx]
            observed_freq[idx + 1] += observed_freq[idx]
    expected_freq_norm = np.delete(expected_freq_norm, idx)
    observed_freq = np.delete(observed_freq, idx)

df_chi2 = len(observed_freq) - 1 - 2
if df_chi2 > 0:
    chi2_stat = np.sum((observed_freq - expected_freq_norm) ** 2 / expected_freq_norm)
    chi2_critical = chi2.ppf(1 - alpha, df_chi2)
    p_chi2 = 1 - chi2.cdf(chi2_stat, df_chi2)
    print(f"χ² = {chi2_stat:.3f}, критическое = {chi2_critical:.3f}, p = {p_chi2:.3f}")
    print(f"Вывод: {'НЕ отвергается' if chi2_stat < chi2_critical else 'отвергается'}")
else:
    print("Недостаточно данных для χ²-теста")

print(f"\n2) Критерий Шапиро-Уилка (n = {n}):")
